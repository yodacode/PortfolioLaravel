	/**
	 * Store a newly created resource in storage.
	 *
	 * @return Response
	 */
	public function store()
	{
		if (Request::ajax())
		{
		    $validator = Validator::make(Input::all(), Tag::$rules);
		    if ($validator->fails()) {
		    	return Response::json(['response' => 'error']);
		    } else {
		    	$cat = new Category;
		    	$cat->title = Input::get('title');
		    	$cat->save();
		    	return Response::json($cat);
		    }
		}
	}

